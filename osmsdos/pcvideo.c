/* Begin graphics mode */
void v_init(void)
{
	__dpmi_regs regs;

	//logfile = fopen("debug.txt", "w");

	find_mode();

	memset(&regs, 0, sizeof(regs));
	if (mode_num < 0x100)
	{
		regs.x.ax = mode_num;
	}
	else
	{
		regs.x.ax = 0x4F02;
		regs.x.bx = mode_num;
	}
	__dpmi_int(0x10, &regs);
	setup_mode();

	cursor_visible = 1;
	cursor_start = CHAR_HEIGHT * 3 / 4;
	cursor_row = 0;
	cursor_col = 0;
	flush_col = 0;
	vmode = 0x07;
}

/* End graphics mode */
void v_reset(void)
{
	__dpmi_regs regs;

	//fclose(logfile);

	memset(&regs, 0, sizeof(regs));
	regs.x.ax = 0x0003;
	__dpmi_int(0x10, &regs);
	free(char_buf);
	free_lfb();
}

/* Make cursor big */
void v_cb(void)
{
	v_flush();
	cursor_off();
	cursor_start = 0;
	cursor_on();
}

/* Make cursor small */
void v_cs(void)
{
	v_flush();
	cursor_off();
	cursor_start = CHAR_HEIGHT * 3 / 4;
	cursor_on();
}

/* Clear to the end of the line */
void v_ce(void)
{
	unsigned row_start;
	unsigned i;

	/* Clear characters in the character buffer */
	v_flush();
	row_start = cursor_row * text_width;
	for (i = cursor_col; i < text_width; ++i)
	{
		char_buf[row_start + i] = 0x0720;
	}

	/* Redraw the cleared character cells */
	render_text(cursor_row, cursor_col, text_width - cursor_col);
}

/* Clear screen and move cursor to home */
void v_cl(void)
{
	cursor_row = 0;
	cursor_col = 0;
	flush_col = 0;
	clear_rows(0, text_height);
}

/* Insert n rows */
void v_al(int n)
{
	unsigned from_row, to_row;
	unsigned from, to;
	unsigned size;
	unsigned i;

	v_flush();

	/* Don't let n run off the end of the screen */
	if (n > text_height - cursor_row)
	{
		n = text_height - cursor_row;
	}

	/* Determine the locations to move and the length */
	from_row = cursor_row;
	to_row = cursor_row + n;
	size = text_height - to_row;
	from = from_row * text_width;
	to = to_row * text_width;
	size *= text_width;

	/* Move the text to be scrolled down */
	memmove(char_buf + to, char_buf + from, sizeof(char_buf[0]) * size);

	/* Fill the vacated rows */
	for (i = from; i < to; ++i)
	{
		char_buf[i] = 0x0720;
	}

	/* Redraw the affected rows */
	for (i = cursor_row; i < text_height; ++i)
	{
		render_text(i, 0, text_width);
	}
}

/* Delete n rows */
void v_dl(int n)
{
	unsigned from_row, to_row, size_rows;
	unsigned from, to, size;
	unsigned i;

	v_flush();

	/* Don't let n run off the end of the screen */
	if (n > text_height - cursor_row)
	{
		n = text_height - cursor_row;
	}

	/* Determine the locations to move and the length */
	to_row = cursor_row;
	from_row = cursor_row + n;
	size_rows = text_height - from_row;
	to = to_row * text_width;
	from = from_row * text_width;
	size = size_rows * text_width;

	/* Move the text to be scrolled up */
	memmove(char_buf + to, char_buf + from, sizeof(char_buf[0]) * size);

	/* Fill the vacated rows */
	for (i = to + size; i < text_height * text_width; ++i)
	{
		char_buf[i] = 0x0720;
	}

	/* Redraw the affected rows */
	for (i = cursor_row; i < text_height; ++i)
	{
		render_text(i, 0, text_width);
	}
}

/* Move cursor to column x, y */
void v_move(int x, int y)
{
	if (x != cursor_col || y != cursor_row)
	{
		v_flush();
		cursor_off();
		cursor_col = x;
		flush_col = x;
		cursor_row = y;
		cursor_on();
	}
}

/* Set the character attribute byte */
void v_attr(int mask, int bits)
{
	vmode = ((vmode & ~mask) ^ bits) & 0xFF;
}

/* Write a single character to display */
int v_put(int ch)
{
	ch &= 0xFF;
	if (ch == '\r')
	{
		v_flush();
		cursor_off();
		cursor_col = 0;
		flush_col = 0;
		cursor_on();
	}
	else if (ch == '\n')
	{
		v_flush();
		if (cursor_row + 1 < text_height)
		{
			cursor_off();
			++cursor_row;
			cursor_on();
		}
	}
	else if (ch == '\a')
	{
		v_flush();
		fputc('\a', stdout);
	}
	else
	{
		unsigned pos = cursor_row * text_width + cursor_col;

		char_buf[pos] = (vmode << 8) | ch;
		if (cursor_col + 1 >= text_width)
		{
			v_flush();
			cursor_off();
			cursor_col = 0;
			flush_col = 0;
			++cursor_row;
			if (cursor_row >= text_height)
				cursor_row = text_height - 1;
			cursor_on();
		}
		else
		{
        		++cursor_col;
			/* Don't render here, but wait for v_flush() */
		}
	}
	return ch;
}

/* Update the display after calls to v_put() */
void v_flush(void)
{
	cursor_visible = 1;
	render_text(cursor_row, flush_col, cursor_col - flush_col + 1);
	flush_col = cursor_col;
}

/* Return the number of column on the display */
int v_cols(void)
{
	return text_width;
}

/* Return the number of rows on the display */
int v_rows(void)
{
	return text_height;
}

/* Turn off the cursor */
static void cursor_off(void)
{
	cursor_visible = 0;
	render_text(cursor_row, cursor_col, 1);
}

/* Turn on the cursor */
static void cursor_on(void)
{
	cursor_visible = 1;
	render_text(cursor_row, cursor_col, 1);
}

/* Clear rows of the screen */
static void clear_rows(unsigned start_row, unsigned end_row)
{
	unsigned row_start;
	unsigned i, j;

	row_start = start_row * text_width;
	for (i = start_row; i < end_row; ++i)
	{
		for (j = 0; j < text_width; ++j)
		{
			char_buf[row_start+j] = 0x0720;
		}
		render_text(i, 0, text_width);
	}
}

/* Find a video mode to use */
static void find_mode(void)
{
	int vbe_info_sel = -1; /* custodial */
	int vbe_info_seg;
	int mode_info_sel = -1; /* custodial */
	int mode_info_seg;
	struct VbeInfoBlock vbe_info;
	unsigned mode_num0;
	unsigned size, size0;
	struct ModeInfoBlock mode_info0;
	unsigned long mode_addr;
	unsigned target_width, target_height;
	unsigned pass;
	__dpmi_regs regs;

	mode_num = 0xFFFF;
	size = 0;

	/* Set target screen size */
	const char *str = getenv("ELVIS_COLS");
	target_width = 0;
	if (str != NULL)
		target_width = strtol(str, NULL, 10) * CHAR_WIDTH;
	if (target_width == 0)
		target_width = VIDEO_WIDTH;
	str = getenv("ELVIS_ROWS");
	target_height = 0;
	if (str != NULL)
		target_height = strtol(str, NULL, 10) * CHAR_HEIGHT;
	if (target_height == 0)
		target_height = VIDEO_HEIGHT;

	/* Allocate transfer areas in DOS memory */
	vbe_info_seg = __dpmi_allocate_dos_memory(
	        (sizeof(vbe_info) + 15) / 16,
	        &vbe_info_sel);
	if (vbe_info_seg < 0) goto end;

	mode_info_seg = __dpmi_allocate_dos_memory(
	        (sizeof(mode_info0) + 15) / 16,
	        &mode_info_sel);
	if (mode_info_seg < 0) goto end;

	/* Request VBE 2.0 information if it is available */
	memset(&vbe_info, 0, sizeof(vbe_info));
	memcpy(vbe_info.VbeSignature, "VBE2", 4);
	dosmemput(&vbe_info, sizeof(vbe_info), vbe_info_seg * 16L);

	/* Request VESA BIOS information */
	memset(&regs, 0, sizeof(regs));
	regs.x.ax = 0x4F00;
	regs.x.di = 0;
	regs.x.es = vbe_info_seg;
	__dpmi_int(0x10, &regs);

	/* Check for successful completion */
	if (regs.x.ax != 0x004F) goto end;
	dosmemget(vbe_info_seg * 16L, sizeof(vbe_info), &vbe_info);
	if (memcmp(vbe_info.VbeSignature, "VESA", 4) != 0) goto end;
	vbe_version = vbe_info.VbeVersion;

	for (pass = 0; pass < 2 && mode_num == 0xFFFF; ++pass)
	{
		if (pass == 1)
		{
			/* We'll use standard VGA mode 0x0012 if nothing preferable is found */
			mode_num = 0x0012;
			size = 640 * 480;
			mode_info.BitsPerPixel = 4;
		}

		/* Get the address of the mode list */
		/* The mode list may be within the DOS memory area allocated above.
		   That area must remain allocated and must not be rewritten until
		   we're done here. */
		mode_addr = (vbe_info.VideoModePtr >> 16) * 16L
			  + (vbe_info.VideoModePtr & 0xFFFF);

		/* Scan through the modes; look for an acceptable mode with the most
		   pixels and the fewest bits per pixel */
		while (1)
		{
			/* Retrieve mode number, and stop when the sentinel is found */
			mode_num0 = _farpeekw(_go32_conventional_mem_selector(),
				mode_addr);
			if (mode_num0 == 0xFFFF)
				break;
			mode_addr += 2;

			/* Retrieve mode information */
			memset(&regs, 0, sizeof(regs));
			regs.x.ax = 0x4F01;
			regs.x.cx = mode_num0;
			regs.x.di = 0;
			regs.x.es = mode_info_seg;
			__dpmi_int(0x10, &regs);

			/* Check that the mode is acceptable */
			if (regs.x.ax != 0x004F)
				continue;
			dosmemget(mode_info_seg * 16L, sizeof(mode_info0), &mode_info0);

			/* Supported, graphics mode, color, VGA compatible */
			if ((mode_info0.ModeAttributes & 0x79) != 0x19)
				continue;
			/* Provide resolution etc. for old BIOSes */
			if (!fill_old_info(mode_num0, &mode_info0))
				continue;
			/* Bits per pixel and memory model are acceptable */
			switch (mode_info0.BitsPerPixel)
			{
			case 4:
				if (mode_info0.MemoryModel != 3) /* Planar */
					continue;
				if (mode_info0.NumberOfPlanes != 4)
					continue;
				break;

			case 8:
				if (mode_info0.MemoryModel != 4) /* Packed pixel */
					continue;
				if (mode_info0.NumberOfPlanes != 1)
					continue;
				break;

			case 15:
			case 16:
			case 24:
			case 32:
				if (mode_info0.MemoryModel != 6) /* Direct color */
					continue;
				if (mode_info0.NumberOfPlanes != 1)
					continue;
				break;

			default:
				continue;
			}

			/* Compare size and pixel depth to that of currently selected mode */
			size0 = mode_info0.XResolution * mode_info0.YResolution;
			if (pass == 0)
			{
				/* Consider only modes at least
				   target_width*target_height, and choose the
				   smallest */
				if (mode_info0.XResolution < target_width
				||  mode_info0.YResolution < target_height)
					continue;
				if ((size0 < size || mode_num == 0xFFFF)
				||  (size0 == size && score(mode_info0.BitsPerPixel) < score(mode_info.BitsPerPixel)))
				{
					mode_num = mode_num0;
					size = size0;
					mode_info = mode_info0;
				}
			}
			else
			{
				/* No mode at least target_width*target_height.
				   Use the largest mode. */
				if (size0 > size
				||  (size0 == size && score(mode_info0.BitsPerPixel) < score(mode_info.BitsPerPixel)))
				{
					mode_num = mode_num0;
					size = size0;
					mode_info = mode_info0;
				}
			}
		}
	}

end:

	if (vbe_info_sel != -1)
		__dpmi_free_dos_memory(vbe_info_sel);
	if (vbe_info_sel != -1)
		__dpmi_free_dos_memory(mode_info_sel);

	/* If no preferable mode is found, fill in information for mode 0x0012 */
	if (mode_num == 0x0012)
	{
		mode_info.ModeAttributes = 0x1F;
		mode_info.WinAttributes[0] = 0x06;
		mode_info.WinAttributes[1] = 0x00;
		mode_info.WinGranularity = 1;
		mode_info.WinSize = (80 * 480 + 1023) / 1024;
		mode_info.WinSegment[0] = 0xA000;
		mode_info.WinSegment[1] = 0;
		mode_info.WinFuncPtr = 0;
		mode_info.BytesPerScanLine = 80;
		mode_info.XResolution = 640;
		mode_info.YResolution = 480;
		mode_info.XCharSize = 8;
		mode_info.YCharSize = 16;
		mode_info.NumberOfPlanes = 4;
		mode_info.BitsPerPixel = 4;
		mode_info.NumberOfBanks = 1;
		mode_info.MemoryModel = 3;
		mode_info.BankSize = 0;
		mode_info.NumberOfImagePages = 1;
	}

	/* Enable the linear frame buffer if it is available */
	if (mode_info.ModeAttributes & 0x80)
		setup_lfb();
}

/* Define the preference among pixel depths:
   fewer bits are preferred to more, except that 4 bit modes are selected
   only after other modes.
   Lower scores are preferred to higher. */
static unsigned score(unsigned bits_per_pixel)
{
	switch (bits_per_pixel)
	{
	case 4:
		return 5;
	case 8:
		return 0;
	case 16:
		return 1; /* Prefer 16 bit to 15 */
	case 15:
		return 2;
	case 24:
		return 3;
	case 32:
		return 4;
	default:
		return 99;
	}
}

/* Set up the linear frame buffer */
static void setup_lfb(void)
{
	unsigned long win_size;
	unsigned long addr;
	unsigned width;

	lfb_selector = __dpmi_allocate_ldt_descriptors(1);
	if (lfb_selector <= 0)
		goto error;

	width = (vbe_version >= 0x0300)
	        ? mode_info.LinBytesPerScanLine
	        : mode_info.BytesPerScanLine;

	win_size = width * mode_info.YResolution;
	addr = map_frame_buffer(mode_info.PhysBasePtr, win_size);
	if (addr == 0)
		goto error;

	__dpmi_set_segment_base_address(lfb_selector, addr);
	__dpmi_set_segment_limit(lfb_selector, (win_size - 1) | 0xFFF);
	mode_num |= 0x4000;

    return;

error:
	/* On error, fall back to the windowed frame buffer */
	if (lfb_selector > 0)
	{
		__dpmi_free_ldt_descriptor(lfb_selector);
		lfb_selector = 0;
	}
}

static unsigned long map_frame_buffer(unsigned long phys_addr, unsigned long size)
{
	__dpmi_meminfo info;
	int rc;

	info.handle = 0;
	info.address = phys_addr;
	info.size = size;
	rc = __dpmi_physical_address_mapping(&info);

	return rc == 0 ? info.address : 0;
}

static void free_lfb(void)
{
	if (lfb_selector > 0)
	{
		__dpmi_meminfo info;
        unsigned long addr;

		info.handle = 0;
		__dpmi_get_segment_base_address(lfb_selector, &addr);
		info.address = addr;
		info.size = 0;
		__dpmi_free_physical_address_mapping(&info);
		__dpmi_free_ldt_descriptor(lfb_selector);
	}
}

/* Fill in information that is missing from older VESA BIOSes */
static int fill_old_info(unsigned mode_num, struct ModeInfoBlock *info)
{
	struct OldModeInfo {
		unsigned short mode;

		unsigned short XResolution;
		unsigned short YResolution;
		unsigned char  BitsPerPixel;
	};
	static const struct OldModeInfo old_mode_table[] = {
		{ 0x0100,  640,  400,  8 },
		{ 0x0101,  640,  480,  8 },
		{ 0x0102,  800,  600,  4 },
		{ 0x0103,  800,  600,  8 },
		{ 0x0104, 1024,  768,  4 },
		{ 0x0105, 1024,  768,  8 },
		{ 0x0106, 1280, 1024,  4 },
		{ 0x0107, 1280, 1024,  8 },
		{ 0x010D,  320,  200, 15 },
		{ 0x010E,  320,  200, 16 },
		{ 0x010F,  320,  200, 24 },
		{ 0x0110,  640,  480, 15 },
		{ 0x0111,  640,  480, 16 },
		{ 0x0112,  640,  480, 24 },
		{ 0x0113,  800,  600, 15 },
		{ 0x0114,  800,  600, 16 },
		{ 0x0115,  800,  600, 24 },
		{ 0x0116, 1024,  768, 15 },
		{ 0x0117, 1024,  768, 16 },
		{ 0x0118, 1024,  768, 24 },
		{ 0x0119, 1280, 1024, 15 },
		{ 0x011A, 1280, 1024, 16 },
		{ 0x011B, 1280, 1024, 24 },
		{ 0, 0, 0 }
	};
	unsigned i;

	if ((info->ModeAttributes & 0x2) != 0)
		return 1; /* Information is already present */

	for (i = 0; old_mode_table[i].mode != 0; ++i)
	{
		if (mode_num == old_mode_table[i].mode)
		{
			info->XResolution = old_mode_table[i].XResolution;
			info->YResolution = old_mode_table[i].YResolution;
			info->BitsPerPixel = old_mode_table[i].BitsPerPixel;
			switch (info->BitsPerPixel)
			{
			case 4:
				info->MemoryModel = 3;
				info->NumberOfPlanes = 4;
				break;

			case 8:
				info->MemoryModel = 4;
				info->NumberOfPlanes = 1;
				break;

			case 15:
				info->MemoryModel = 6;
				info->NumberOfPlanes = 1;
				info->RedMaskSize = 5;
				info->RedFieldPosition = 10;
				info->GreenMaskSize = 5;
				info->GreenFieldPosition = 5;
				info->BlueMaskSize = 5;
				info->BlueFieldPosition = 0;
				break;

			case 16:
				info->MemoryModel = 6;
				info->NumberOfPlanes = 1;
				info->RedMaskSize = 5;
				info->RedFieldPosition = 11;
				info->GreenMaskSize = 6;
				info->GreenFieldPosition = 5;
				info->BlueMaskSize = 5;
				info->BlueFieldPosition = 0;
				break;

			case 24:
				info->MemoryModel = 6;
				info->NumberOfPlanes = 1;
				info->RedMaskSize = 8;
				info->RedFieldPosition = 16;
				info->GreenMaskSize = 8;
				info->GreenFieldPosition = 8;
				info->BlueMaskSize = 8;
				info->BlueFieldPosition = 0;
				break;
			}
			info->ModeAttributes |= 0x02;
			return 1;
		}
	}

	return 0; /* no information on this mode */
}

/* Set up static variables for the selected mode */
static void setup_mode(void)
{
	static unsigned char const text_colors[16][3] = {
		{ 0x00, 0x00, 0x00 },
		{ 0x00, 0x00, 0xAA },
		{ 0x00, 0xAA, 0x00 },
		{ 0x00, 0x40, 0x99 },
		{ 0xAA, 0x00, 0x00 },
		{ 0xAA, 0x00, 0xAA },
		{ 0xAA, 0xAA, 0x00 },
		{ 0xAA, 0xAA, 0xAA },
		{ 0x55, 0x55, 0x55 },
		{ 0x00, 0x00, 0xFF },
		{ 0x00, 0xFF, 0x00 },
		{ 0x00, 0xFF, 0xFF },
		{ 0xFF, 0x00, 0x00 },
		{ 0xFF, 0x00, 0xFF },
		{ 0xFF, 0xFF, 0x00 },
		{ 0xFF, 0xFF, 0xFF }
	};
	unsigned i;

	/* Set up parameters for the windowed frame buffer */
	if (mode_num < 0x100)
	{
		read_win = 0;
		write_win = 0;
		win_offset[0] = 0;
		win_offset[1] = 0;
	}
	else
	{
		if (mode_info.WinAttributes[0] & 0x2)
			read_win = 0;
		else
			read_win = 1;
		if (mode_info.WinAttributes[0] & 0x4)
			write_win = 0;
		else
			write_win = 1;
		/* Window offset is initially unknown; move_window will call
		   the BIOS the first time */
		win_offset[0] = 0xFFFFFFFF;
		win_offset[1] = 0xFFFFFFFF;
	}

	/* Allocate space for the text being displayed */
	text_width = mode_info.XResolution / CHAR_WIDTH;
	text_height = mode_info.YResolution / CHAR_HEIGHT;
	char_buf = calloc(text_width*text_height, sizeof(unsigned short));
	if (char_buf == NULL)
	{
		v_reset();
		msg(MSG_FATAL, "Could not allocate memory");
	}
	bytes_per_line = ((mode_num & 0x4000) != 0 && vbe_version >= 0x0300)
	               ? mode_info.LinBytesPerScanLine
	               : mode_info.BytesPerScanLine;

	/* Set up the colors for the selected mode */
	if (mode_info.BitsPerPixel > 8)
	{
		unsigned red_size, green_size, blue_size;
		unsigned red_pos, green_pos, blue_pos;

		if ((mode_num & 0x4000) != 0 && vbe_version >= 0x0300)
		{
			red_size = mode_info.LinRedMaskSize;
			green_size = mode_info.LinGreenMaskSize;
			blue_size = mode_info.LinBlueMaskSize;
			red_pos = mode_info.LinRedFieldPosition;
			green_pos = mode_info.LinGreenFieldPosition;
			blue_pos = mode_info.LinBlueFieldPosition;
		}
		else
		{
			red_size = mode_info.RedMaskSize;
			green_size = mode_info.GreenMaskSize;
			blue_size = mode_info.BlueMaskSize;
			red_pos = mode_info.RedFieldPosition;
			green_pos = mode_info.GreenFieldPosition;
			blue_pos = mode_info.BlueFieldPosition;
		}
		for (i = 0; i < 16; ++i)
		{
			unsigned r = text_colors[i][0];
			unsigned g = text_colors[i][1];
			unsigned b = text_colors[i][2];
			r >>= 8 - red_size;
			g >>= 8 - green_size;
			b >>= 8 - blue_size;
			colors[i] = ((unsigned long)r << red_pos)
			          | ((unsigned long)g << green_pos)
			          | ((unsigned long)b << blue_pos);
		}
	}
	else
	{
		for (i = 0; i < 16; ++i)
		{
			colors[i] = i;
		}
	}
}

/* Render text from the char_buf array */
static void render_text(unsigned row, unsigned col, unsigned count)
{
	if (mode_info.BitsPerPixel == 4)
	{
		render_text_4(row, col, count);
	}
	else
	{
		render_text_8(row, col, count);
	}
}

/* Render text from the char_buf array: 4 bit modes */
static void render_text_4(unsigned row, unsigned col, unsigned count)
{
	unsigned plane;

	/* Loop once per plane */
	for (plane = 0; plane < 4; ++plane)
	{
		unsigned addr = row * CHAR_HEIGHT * bytes_per_line
		              + col;
		unsigned line;

		/* Set the current plane */
		outportb(0x3C4, 2);
		outportb(0x3C5, 1 << plane);

		/* Loop once per raster line */
		for (line = 0; line < CHAR_HEIGHT; ++line)
		{
			unsigned cp = addr;
			char bytes[512];
			unsigned num_bytes;
			unsigned pos;
			unsigned i;

			num_bytes = 0;
			pos = row * text_width + col;
			/* Loop once per character */
			for (i = 0; i < count; ++i)
			{
				unsigned ch = char_buf[pos+i];
				unsigned bits = (ch >> plane) & 0x1100;
				unsigned fpos;
				unsigned char fdata;

				if (bits == 0x0100 || bits == 0x1000)
				{
					ch &= 0xFF;
					fpos = ch * CHAR_HEIGHT + line;
					fdata = font[fpos];
					if (cursor_visible
					&&  row == cursor_row
					&&  col + i == cursor_col
					&&  line >= cursor_start)
						fdata ^= 0xFF;
				}
				else
				{
					fdata = 0x00;
				}
				if (bits & 0x1000)
					fdata ^= 0xFF;
				bytes[num_bytes++] = fdata;
				if (num_bytes >= sizeof(bytes))
				{
					video_write(cp, bytes, num_bytes);
					cp += num_bytes;
					num_bytes = 0;
				}
			}
			video_write(cp, bytes, num_bytes);
			addr += bytes_per_line;
		}
	}
}

/* Render text from the char_buf array: 8 bit and direct color modes */
static void render_text_8(unsigned row, unsigned col, unsigned count)
{
	unsigned bytes_per_pixel = (mode_info.BitsPerPixel + 7) / 8;
	unsigned long addr = row * CHAR_HEIGHT * bytes_per_line
	                   + col * CHAR_WIDTH * bytes_per_pixel;
	unsigned long line;

	/* Loop once per raster line */
	for (line = 0; line < CHAR_HEIGHT; ++line)
	{
		unsigned long cp = addr;
		unsigned pos = row * text_width + col;
		unsigned char bytes[512];
		unsigned num_bytes = 0;
		unsigned i;

		/* Loop once per character */
		for (i = 0; i < count; ++i)
		{
			unsigned ch = char_buf[pos + i];
			unsigned long fore = colors[(ch >>  8) & 0x0F];
			unsigned long back = colors[(ch >> 12) & 0x0F];
			unsigned fpos = (ch & 0xFF) * CHAR_HEIGHT + line;
			unsigned char fdata = font[fpos];
			unsigned char bit;

			if (cursor_visible
			&&  row == cursor_row && col + i == cursor_col
			&&  line >= cursor_start)
				fdata ^= 0xFF;

			/* Loop once per bit */
			for (bit = 0x80; bit != 0; bit >>= 1)
			{
				unsigned long c = (fdata & bit) ? fore : back;
				*(unsigned long *)(bytes + num_bytes) = c;
				num_bytes += bytes_per_pixel;
				if (num_bytes + sizeof(unsigned long)
				>   sizeof(bytes))
				{
					video_write(cp, bytes, num_bytes);
					cp += num_bytes;
					num_bytes = 0;
				}
			}
		}
		video_write(cp, bytes, num_bytes);

		addr += bytes_per_line;
	}
}

/* Set the window for access to the frame buffer */
static unsigned move_window(unsigned win, unsigned long addr)
{
	unsigned long pos = win_offset[win];
	if (addr < pos || pos + mode_info.WinSize <= addr)
	{
		/* Given address is not within the current window */
		__dpmi_regs regs;
		unsigned gran = mode_info.WinGranularity * 1024;

		memset(&regs, 0, sizeof(regs));
		regs.x.ax = 0x4F05;
		regs.h.bh = 0x00;
		regs.h.bl = win;
		regs.x.dx = addr / gran;
		pos = regs.x.dx * gran;
		__dpmi_int(0x10, &regs);
		win_offset[win] = pos;
	}
	return (unsigned)(addr - pos);
}

/* Write bytes to the frame buffer */
static void video_write(unsigned long addr, const void *data, size_t size)
{
	if (mode_num & 0x4000)
	{
		movedata(_go32_my_ds(), (unsigned)data,
		         lfb_selector, addr, size);
	}
	else
	{
		/* More than one pass is needed if the write crosses a window
		   boundary */
		while (size != 0)
		{
			unsigned offset = move_window(write_win, addr);
			unsigned size2 = (mode_info.WinSize * 1024) - offset;
			if (size2 > size)
				size2 = size;
			dosmemput(data, size2,
				  mode_info.WinSegment[write_win]*16L + offset);
			addr += size2;
			data = (const char *)data + size2;
			size -= size2;
		}
	}
}

